import SwiftUI

struct CompleteWordListView: View {
    var completeWordList: [String]
    
    var body: some View {
        VStack(alignment: .leading) {
            Text("Complete Word List")
                .font(.headline)
            ScrollView(showsIndicators: true) {
                ForEach(completeWordList, id: \.self) { word in
                    Text(word)
                }
            }
        }
    }
}

